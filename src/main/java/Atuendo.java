class Atuendo {

  Prenda prendaSup;
  Prenda prendaInf;
  Prenda calzado;
  Prenda accesorio;

  Atuendo(Prenda prendaSup, Prenda prendaInf, Prenda calzado, Prenda accesorio) {
    this.prendaSup = prendaSup;
    this.prendaInf = prendaInf;
    this.calzado = calzado;
    this.accesorio = accesorio;
  }
}
